-- ============================================================================
-- 004 「困った起点の学習ループ」用に困った回数を永続化
-- ============================================================================
-- 問題: step_progress は (session_id, step_id) 1行を upsert で上書きするため、
--       困った(stuck)→ 後で 完了(done) すると stuck の痕跡が消え、
--       訓練後の「困った箇所(改善のヒント)」が空になっていた。
--
-- 対策: stuck_count 列を追加。困った を押すたびに +1 し、done/skip では
--       この列に触れない(= 完了しても困った回数は残る)。
--
-- 安全性: NOT NULL DEFAULT 0 なので既存行は 0 で埋まり、列を payload に
--         含めない既存の upsert は影響を受けない(本番を壊さない)。
--         IF NOT EXISTS なので2回流しても安全。
-- ============================================================================

ALTER TABLE public.step_progress
  ADD COLUMN IF NOT EXISTS stuck_count integer NOT NULL DEFAULT 0;
